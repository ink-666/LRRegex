using System; // Подключает базовые возможности C#, например Console и Math
using System.Collections.Generic; // Подключает коллекции, например List<T>; в этом коде пока не используется
using System.IO; // Нужен для работы с файлами: File.Exists, File.ReadAllText, File.ReadAllLines
using System.Text.RegularExpressions; // Подключает регулярные выражения: Regex, MatchCollection, Match

// Пространство имён объединяет классы этой лабораторной работы
namespace RegexLab
{
    // Основной класс программы
    class Program
    {
        // Главный метод программы, с него начинается выполнение
        static void Main(string[] args)
        {
            // Выводим верхнюю границу заголовка
            Console.WriteLine("==========================================");

            // Выводим название лабораторной работы
            Console.WriteLine("ЛАБОРАТОРНАЯ РАБОТА №6: РЕГУЛЯРНЫЕ ВЫРАЖЕНИЯ");

            // Выводим нижнюю границу заголовка и пустую строку
            Console.WriteLine("==========================================\n");

            // ==========================================================
            // ЧАСТЬ 1: ЗАГРУЗКА ТЕКСТА ИЗ ФАЙЛА
            // ==========================================================

            // Выводим заголовок первой части
            Console.WriteLine("📁 ЧАСТЬ 1: ЗАГРУЗКА ТЕКСТА");

            // Выводим разделитель
            Console.WriteLine("==========================================\n");

            // Указываем имя файла, из которого будет читаться текст
            string filePath = "text.txt";

            // Проверяем, существует ли файл text.txt рядом с программой
            if (!File.Exists(filePath))
            {
                // Сообщаем пользователю, что файл не найден
                Console.WriteLine($"❌ Файл {filePath} не найден!");

                // Подсказываем, какой файл нужно создать
                Console.WriteLine("Создайте файл text.txt с текстом для анализа.");

                // Завершаем программу, потому что без файла анализировать нечего
                return;
            }

            // Читаем весь текст из файла одной строкой
            string text = File.ReadAllText(filePath);

            // Читаем файл построчно в массив строк
            string[] lines = File.ReadAllLines(filePath);

            // Сообщаем, из какого файла загружен текст
            Console.WriteLine($"📄 Загружен текст из файла: {filePath}");

            // Выводим количество строк в файле
            Console.WriteLine($"📊 Всего строк: {lines.Length}");

            // Выводим количество всех символов в тексте
            Console.WriteLine($"📊 Всего символов: {text.Length}");

            // Выводим подпись перед фрагментом текста
            Console.WriteLine("\n📝 Первые 100 символов текста:");

            // Показываем первые 100 символов или меньше, если текст короче
            Console.WriteLine(text.Substring(0, Math.Min(100, text.Length)) + "...\n");

            // ==========================================================
            // ЧАСТЬ 2: ПОИСК КОЛИЧЕСТВА СЛОВ
            // ==========================================================

            // Выводим заголовок второй части
            Console.WriteLine("\n🔍 ЧАСТЬ 2: ПОДСЧЁТ КОЛИЧЕСТВА СЛОВ");

            // Выводим разделитель
            Console.WriteLine("==========================================");

            // Регулярное выражение для поиска слов из русских и английских букв
            // \b — граница слова, + — одна или больше букв
            string wordPattern = @"\b[а-яА-ЯёЁa-zA-Z]+\b";

            // Ищем в тексте все совпадения по шаблону wordPattern
            MatchCollection wordMatches = Regex.Matches(text, wordPattern);

            // Показываем использованное регулярное выражение
            Console.WriteLine($"\n📌 Регулярное выражение: {wordPattern}");

            // Выводим количество найденных слов
            Console.WriteLine($"📊 Всего слов в тексте: {wordMatches.Count}");

            // Выводим подпись перед списком первых слов
            Console.WriteLine("\n📝 Первые 10 слов:");

            // Цикл выводит первые 10 слов или меньше, если слов найдено меньше 10
            for (int i = 0; i < Math.Min(10, wordMatches.Count); i++)
            {
                // Выводим номер слова и само найденное слово
                Console.WriteLine($"   {i + 1}. {wordMatches[i].Value}");
            }

            // ==========================================================
            // ЧАСТЬ 3: ПОИСК ОПРЕДЕЛЁННЫХ СЛОВОСОЧЕТАНИЙ
            // ==========================================================

            // Выводим заголовок третьей части
            Console.WriteLine("\n\n🔍 ЧАСТЬ 3: ПОИСК СЛОВОСОЧЕТАНИЙ");

            // Выводим разделитель
            Console.WriteLine("==========================================");

            // Шаблон ищет слово "не" и следующее за ним русское слово
            // \s+ означает один или несколько пробелов между словами
            string phrasePattern = @"\bне\s+[а-яА-ЯёЁ]+\b";

            // Ищем все такие словосочетания, не учитывая регистр букв
            MatchCollection phraseMatches = Regex.Matches(text, phrasePattern, RegexOptions.IgnoreCase);

            // Показываем регулярное выражение для поиска словосочетаний с "не"
            Console.WriteLine($"\n📌 Поиск словосочетаний с 'не': {phrasePattern}");

            // Выводим количество найденных словосочетаний
            Console.WriteLine($"📊 Найдено: {phraseMatches.Count}");

            // Перебираем все найденные совпадения
            foreach (Match match in phraseMatches)
            {
                // Выводим найденное словосочетание
                Console.WriteLine($"   ➡️ {match.Value}");
            }

            // Шаблон ищет сочетания вида "слово и слово"
            string andPattern = @"\b[а-яА-ЯёЁ]+\s+и\s+[а-яА-ЯёЁ]+\b";

            // Ищем все сочетания с союзом "и", не учитывая регистр букв
            MatchCollection andMatches = Regex.Matches(text, andPattern, RegexOptions.IgnoreCase);

            // Показываем шаблон для поиска сочетаний "слово и слово"
            Console.WriteLine($"\n📌 Поиск сочетаний 'слово и слово': {andPattern}");

            // Выводим количество найденных сочетаний
            Console.WriteLine($"📊 Найдено: {andMatches.Count}");

            // Перебираем все найденные сочетания
            foreach (Match match in andMatches)
            {
                // Выводим найденное совпадение
                Console.WriteLine($"   ➡️ {match.Value}");
            }

            // ==========================================================
            // ЧАСТЬ 4: ПОИСК ОПРЕДЕЛЁННЫХ СИМВОЛОВ
            // ==========================================================

            // Выводим заголовок четвёртой части
            Console.WriteLine("\n\n🔍 ЧАСТЬ 4: ПОИСК ОПРЕДЕЛЁННЫХ СИМВОЛОВ");

            // Выводим разделитель
            Console.WriteLine("==========================================");

            // Шаблон ищет знаки препинания: точку, запятую, точку с запятой и другие
            string punctuationPattern = @"[.,;:!?-]";

            // Ищем все знаки препинания в тексте
            MatchCollection punctuationMatches = Regex.Matches(text, punctuationPattern);

            // Показываем шаблон для поиска знаков препинания
            Console.WriteLine($"\n📌 Знаки препинания: {punctuationPattern}");

            // Выводим количество найденных знаков препинания
            Console.WriteLine($"📊 Всего знаков препинания: {punctuationMatches.Count}");

            // Шаблон ищет русские гласные буквы в верхнем и нижнем регистре
            string vowelsPattern = @"[аеёиоуыэюяАЕЁИОУЫЭЮЯ]";

            // Ищем все гласные буквы в тексте
            MatchCollection vowelsMatches = Regex.Matches(text, vowelsPattern);

            // Показываем шаблон для поиска гласных
            Console.WriteLine($"\n📌 Гласные буквы: {vowelsPattern}");

            // Выводим количество найденных гласных
            Console.WriteLine($"📊 Всего гласных: {vowelsMatches.Count}");

            // Шаблон ищет русские согласные буквы в верхнем и нижнем регистре
            string consonantsPattern = @"[бвгджзйклмнпрстфхцчшщБВГДЖЗЙКЛМНПРСТФХЦЧШЩ]";

            // Ищем все согласные буквы в тексте
            MatchCollection consonantsMatches = Regex.Matches(text, consonantsPattern);

            // Показываем шаблон для поиска согласных
            Console.WriteLine($"\n📌 Согласные буквы: {consonantsPattern}");

            // Выводим количество найденных согласных
            Console.WriteLine($"📊 Всего согласных: {consonantsMatches.Count}");

            // ==========================================================
            // ЧАСТЬ 5: СТРОКИ, НАЧИНАЮЩИЕСЯ С ОПРЕДЕЛЁННОГО СЛОВА
            // ==========================================================

            // Выводим заголовок пятой части
            Console.WriteLine("\n\n🔍 ЧАСТЬ 5: СТРОКИ, НАЧИНАЮЩИЕСЯ С 'Мой'");

            // Выводим разделитель
            Console.WriteLine("==========================================");

            // Шаблон ищет строки, которые начинаются со слова "Мой"
            // ^ означает начало строки, .* означает любые символы после слова
            string startPattern = @"^Мой.*";

            // Показываем шаблон поиска
            Console.WriteLine($"📌 Регулярное выражение: {startPattern}\n");

            // Счётчик строк, найденных по условию
            int foundStartLines = 0;

            // Проходим по всем строкам файла
            for (int i = 0; i < lines.Length; i++)
            {
                // Проверяем, начинается ли текущая строка со слова "Мой"
                if (Regex.IsMatch(lines[i], startPattern, RegexOptions.IgnoreCase))
                {
                    // Выводим номер строки и её содержимое
                    Console.WriteLine($"   Строка {i + 1}: {lines[i]}");

                    // Увеличиваем счётчик найденных строк
                    foundStartLines++;
                }
            }

            // Если подходящих строк не найдено
            if (foundStartLines == 0)
            {
                // Выводим сообщение об отсутствии результата
                Console.WriteLine("   Строк, начинающихся с 'Мой', не найдено.");
            }

            // ==========================================================
            // ЧАСТЬ 6: СТРОКИ, ОКАНЧИВАЮЩИЕСЯ ОПРЕДЕЛЁННЫМ СИМВОЛОМ
            // ==========================================================

            // Выводим заголовок шестой части
            Console.WriteLine("\n\n🔍 ЧАСТЬ 6: СТРОКИ, ОКАНЧИВАЮЩИЕСЯ НА ';' ИЛИ ','");

            // Выводим разделитель
            Console.WriteLine("==========================================");

            // Шаблон ищет символ ; или ,
            string endPattern = @"[;,]";

            // Показываем шаблон с добавленным знаком конца строки $
            Console.WriteLine($"📌 Регулярное выражение: {endPattern}$\n");

            // Счётчик строк, которые заканчиваются на ; или ,
            int foundEndLines = 0;

            // Проходим по всем строкам файла
            for (int i = 0; i < lines.Length; i++)
            {
                // Trim убирает пробелы по краям строки
                // $ означает, что ; или , должны стоять именно в конце строки
                if (Regex.IsMatch(lines[i].Trim(), endPattern + "$"))
                {
                    // Выводим номер найденной строки и её текст
                    Console.WriteLine($"   Строка {i + 1}: {lines[i]}");

                    // Увеличиваем счётчик найденных строк
                    foundEndLines++;
                }
            }

            // Если подходящих строк нет
            if (foundEndLines == 0)
            {
                // Сообщаем, что строки не найдены
                Console.WriteLine("   Строк, оканчивающихся на ';' или ',', не найдено.");
            }

            // ==========================================================
            // ЧАСТЬ 7: ЗАМЕНА ЧАСТИ ТЕКСТА
            // ==========================================================

            // Выводим заголовок седьмой части
            Console.WriteLine("\n\n✏️ ЧАСТЬ 7: ЗАМЕНА ЧАСТИ ТЕКСТА");

            // Выводим разделитель
            Console.WriteLine("==========================================");

            // Шаблон ищет отдельное слово "не"
            // \b нужны, чтобы не заменить "не" внутри других слов
            string oldPattern = @"\bне\b";

            // Текст, на который будет заменено найденное слово
            string replacement = "НИ";

            // Выполняем замену всех отдельных слов "не" на "НИ", не учитывая регистр
            string modifiedText = Regex.Replace(text, oldPattern, replacement, RegexOptions.IgnoreCase);

            // Показываем, что именно заменяется
            Console.WriteLine($"📌 Замена: '{oldPattern}' → '{replacement}'\n");

            // Выводим подпись перед результатом
            Console.WriteLine("📝 Текст после замены (первые 200 символов):");

            // Показываем первые 200 символов изменённого текста или меньше, если текст короче
            Console.WriteLine(modifiedText.Substring(0, Math.Min(200, modifiedText.Length)) + "...");

            // ==========================================================
            // ЧАСТЬ 8: ДОПОЛНИТЕЛЬНЫЕ ПРИМЕРЫ
            // ==========================================================

            // Выводим заголовок восьмой части
            Console.WriteLine("\n\n✨ ЧАСТЬ 8: ДОПОЛНИТЕЛЬНЫЕ ПРИМЕРЫ");

            // Выводим разделитель
            Console.WriteLine("==========================================");

            // Шаблон ищет русские слова, которые начинаются с большой буквы
            string capitalPattern = @"\b[А-ЯЁ][а-яё]*\b";

            // Ищем все слова с большой буквы
            MatchCollection capitalMatches = Regex.Matches(text, capitalPattern);

            // Показываем шаблон поиска слов с большой буквы
            Console.WriteLine($"\n📌 Слова с большой буквы: {capitalPattern}");

            // Выводим количество найденных слов
            Console.WriteLine($"📊 Найдено: {capitalMatches.Count}");

            // Цикл показывает первые 5 найденных слов
            for (int i = 0; i < Math.Min(5, capitalMatches.Count); i++)
            {
                // Выводим номер и значение найденного слова
                Console.WriteLine($"   {i + 1}. {capitalMatches[i].Value}");
            }

            // Шаблон ищет русские слова ровно из 5 букв
            string fiveLetterPattern = @"\b[а-яА-ЯёЁ]{5}\b";

            // Ищем все слова длиной 5 букв
            MatchCollection fiveLetterMatches = Regex.Matches(text, fiveLetterPattern);

            // Показываем шаблон поиска слов из 5 букв
            Console.WriteLine($"\n📌 Слова из 5 букв: {fiveLetterPattern}");

            // Выводим количество найденных слов
            Console.WriteLine($"📊 Найдено: {fiveLetterMatches.Count}");

            // Перебираем все найденные слова из 5 букв
            foreach (Match match in fiveLetterMatches)
            {
                // Выводим найденное слово
                Console.WriteLine($"   ➡️ {match.Value}");
            }

            // ==========================================================
            // ИТОГИ
            // ==========================================================

            // Выводим длинный разделитель из символов =
            Console.WriteLine("\n" + "=".PadRight(60, '='));

            // Выводим заголовок итоговой статистики
            Console.WriteLine("📊 ИТОГОВАЯ СТАТИСТИКА");

            // Выводим ещё один разделитель
            Console.WriteLine("=".PadRight(60, '='));

            // Выводим количество строк
            Console.WriteLine($"\n📄 Всего строк в тексте: {lines.Length}");

            // Выводим количество символов
            Console.WriteLine($"📝 Всего символов: {text.Length}");

            // Выводим количество слов
            Console.WriteLine($"🔤 Всего слов: {wordMatches.Count}");

            // Выводим количество знаков препинания
            Console.WriteLine($"🔣 Знаков препинания: {punctuationMatches.Count}");

            // Выводим количество гласных
            Console.WriteLine($"🅰️ Гласных букв: {vowelsMatches.Count}");

            // Выводим количество согласных
            Console.WriteLine($"🅱️ Согласных букв: {consonantsMatches.Count}");

            // Выводим разделитель
            Console.WriteLine("\n" + "=".PadRight(60, '='));

            // Сообщаем, что программа завершилась
            Console.WriteLine("✅ ПРОГРАММА ЗАВЕРШЕНА");

            // Выводим финальный разделитель
            Console.WriteLine("=".PadRight(60, '='));
        }
    }
}
